const int MOD = 998244353;

int binpow(int a, int n) { // a^n
    if (n == 0) return 1;
    if (n % 2 == 0) {
        return binpow(a * a % MOD, n / 2);
    }
    else {
        return a * binpow(a, n - 1) % MOD;
    }
}

