const int MOD = 1e9 + 7;

int bpow(int a, int b) {
    a %= MOD;
    int res = 1;
    while (b > 0) {
        if (b & 1) {
            res = res * a % MOD;
        }
        a = a * a % MOD;
        b >>= 1;
    }
    return res;
}

uci main() {
    ios_base::sync_with_stdio(0); cin.tie(0); cout.tie(0);

    int t;
    cin >> t;

    while (t--) {
        int l, r, k;
        cin >> l >> r >> k;

        int ans = bpow(9 / k + 1, r) - bpow(9 / k + 1, l);

        cout << (ans + MOD) % MOD << '\n';
    }
}
